let age = 10;

// Relational
age > 5; // true
age < 5; // false
age >= 10; // true
age <= 5; // false

// Equality / שיוויון
age === 10; // true
age !== 10; // false

// Strict Equality operator
console.log("10" === 10); // false (string is not equal number)

// Lose Equality operator
console.log("10" == 10); // true ("10" == "10")
