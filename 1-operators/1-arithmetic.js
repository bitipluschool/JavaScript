/*  ---  JavaScript - Operators ---  */
/*  ---  Arithmetic  ---  */

let x = 10;
let y = 5;

x + y; // חיבור (Addition)
x - y; // חיסור (Subtraction)
x * y; // כפל (Multiplication)
x / y; // חילוק (Division)
x % y; // שארית (Modulus)
x ** y; // חזקה (Exponentiation)

x++; // x = x + 1 // תוספת (Increment)
x--; // x = x - 1 // צמצום (Decrement)

console.log(x++); // 11
console.log(++x); // 10

"Code" + 7; // Code7
"Code" + 20 + 5; // Code205
20 + 5 + "Code"; // 25Code
