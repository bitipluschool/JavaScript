/*  ---  JavaScript Basics - Adding Elements  ---  */
/**
 * Parameters:
 * 1. item - The item to search for
 * 2. start - Where to start the search (default = 0)
 */
const list = [1, 2, 3, 4, 2];

// Returns the index of the first occurrence of a value in an array.
list.indexOf(2, 0); // 1

list.lastIndexOf(2); // 4

// the two above Returns -1 if the item is not found.

// Determines whether an array includes a certain element. retured boolean
list.includes();
